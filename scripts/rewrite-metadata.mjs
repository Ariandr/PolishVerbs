import { spawn } from 'node:child_process'
import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

const rootDir = process.cwd()
const verbsDir = path.join(rootDir, 'src/data/verbs')
const cacheDir = path.join(rootDir, 'node_modules/.tmp')
const cachePath = path.join(cacheDir, 'rewrite-metadata-cache.json')
const outputPath = path.join(cacheDir, 'rewrite-metadata-output.json')

const minRank = Number(process.env.MIN_RANK ?? 3001)
const maxRank = Number(process.env.MAX_RANK ?? 5000)
const rankList = (process.env.RANKS ?? '')
  .split(',')
  .map((token) => Number(token.trim()))
  .filter((value) => Number.isInteger(value) && value > 0)
const rankSet = rankList.length > 0 ? new Set(rankList) : null
const batchSize = Number(process.env.BATCH_SIZE ?? 5)
const codexModel = process.env.CODEX_MODEL ?? 'gpt-5.2'
const maxRetries = Number(process.env.MAX_RETRIES ?? 2)
const batchTimeoutMs = Number(process.env.BATCH_TIMEOUT_MS ?? 600_000)
const stopOnBatchFailure = process.env.STOP_ON_BATCH_FAILURE !== '0'

const aspectValues = new Set(['imperfective', 'perfective', 'biaspectual'])
const pronouns = ['ja', 'ty', 'on', 'ona', 'my', 'wy', 'oni', 'one']

function cleanString(value) {
  return String(value ?? '').replace(/\s+/g, ' ').trim()
}

function needsRewrite(verb) {
  return (
    verb.aspect === 'unknown' ||
    verb.reviewStatus === 'fallback-needs-review' ||
    (verb.notes ?? []).some((note) => /generated by fallback|should be checked/i.test(note))
  )
}

function isPastShape(value) {
  return (
    cleanString(value?.ja?.masculine) &&
    cleanString(value?.ja?.feminine) &&
    cleanString(value?.ty?.masculine) &&
    cleanString(value?.ty?.feminine) &&
    cleanString(value?.on?.masculine) &&
    cleanString(value?.ona?.feminine) &&
    cleanString(value?.my?.virile) &&
    cleanString(value?.my?.nonvirile) &&
    cleanString(value?.wy?.virile) &&
    cleanString(value?.wy?.nonvirile) &&
    cleanString(value?.oni?.virile) &&
    cleanString(value?.one?.nonvirile)
  )
}

function sanitizeCandidate(candidate) {
  return {
    aspect: cleanString(candidate?.aspect),
    aspectPair: cleanString(candidate?.aspectPair),
    forms: {
      present: Object.fromEntries(pronouns.map((pronoun) => [pronoun, cleanString(candidate?.forms?.present?.[pronoun])])),
      past: {
        ja: {
          masculine: cleanString(candidate?.forms?.past?.ja?.masculine),
          feminine: cleanString(candidate?.forms?.past?.ja?.feminine),
        },
        ty: {
          masculine: cleanString(candidate?.forms?.past?.ty?.masculine),
          feminine: cleanString(candidate?.forms?.past?.ty?.feminine),
        },
        on: { masculine: cleanString(candidate?.forms?.past?.on?.masculine) },
        ona: { feminine: cleanString(candidate?.forms?.past?.ona?.feminine) },
        my: {
          virile: cleanString(candidate?.forms?.past?.my?.virile),
          nonvirile: cleanString(candidate?.forms?.past?.my?.nonvirile),
        },
        wy: {
          virile: cleanString(candidate?.forms?.past?.wy?.virile),
          nonvirile: cleanString(candidate?.forms?.past?.wy?.nonvirile),
        },
        oni: { virile: cleanString(candidate?.forms?.past?.oni?.virile) },
        one: { nonvirile: cleanString(candidate?.forms?.past?.one?.nonvirile) },
      },
    },
  }
}

function isValidCandidate(candidate) {
  const sanitized = sanitizeCandidate(candidate)
  if (!aspectValues.has(sanitized.aspect)) return false
  if (!pronouns.every((pronoun) => cleanString(sanitized.forms.present[pronoun]))) return false
  if (!isPastShape(sanitized.forms.past)) return false
  return true
}

function buildPrompt(batch) {
  const payload = batch.map((verb) => ({
    frequencyRank: verb.frequencyRank,
    infinitive: verb.infinitive,
    currentAspect: verb.aspect,
    translationsEn: (verb.translations?.en ?? []).slice(0, 3),
    translationsUk: (verb.translations?.uk ?? []).slice(0, 3),
    examplePl: verb.examples?.[0]?.pl ?? '',
    currentForms: verb.forms,
  }))

  return [
    'Correct Polish verb metadata and conjugation forms for a learner app.',
    'Return ONLY a JSON array of objects with: frequencyRank, aspect, aspectPair, forms.',
    'Rules:',
    '- Keep output order identical to input order.',
    '- aspect must be exactly one of: imperfective, perfective, biaspectual.',
    '- aspectPair should be the common paired infinitive if known, otherwise an empty string.',
    '- forms.present must contain ja, ty, on, ona, my, wy, oni, one.',
    '- For perfective verbs, forms.present should contain the normal non-past/future simple forms, e.g. zrobię, zrobisz.',
    '- forms.past must contain ja/ty masculine+feminine, on masculine, ona feminine, my/wy virile+nonvirile, oni virile, one nonvirile.',
    '- Use standard contemporary Polish spelling with diacritics.',
    '- Do not add comments, uncertainty notes, alternatives, slashes, or parentheses inside form strings.',
    '',
    `Input: ${JSON.stringify(payload)}`,
  ].join('\n')
}

function isQuotaOrGateError(error) {
  return /usage limit|purchase more credits|try again at|cloudflare|challenge-platform|__cf_chl/i.test(String(error?.message ?? error))
}

function parseJsonArrayFromText(rawText) {
  const trimmed = rawText.trim()
  if (!trimmed) throw new Error('Empty Codex output')
  try {
    const parsed = JSON.parse(trimmed)
    if (Array.isArray(parsed)) return parsed
  } catch {
    // Continue with fence/array extraction.
  }
  const fencedMatch = trimmed.match(/```(?:json)?\s*([\s\S]*?)\s*```/i)
  if (fencedMatch?.[1]) {
    const parsed = JSON.parse(fencedMatch[1])
    if (Array.isArray(parsed)) return parsed
  }
  const arrayMatch = trimmed.match(/\[[\s\S]*\]/)
  if (arrayMatch?.[0]) {
    const parsed = JSON.parse(arrayMatch[0])
    if (Array.isArray(parsed)) return parsed
  }
  throw new Error('Could not parse JSON array from Codex output')
}

async function runCodexBatch(batch) {
  const args = [
    'exec',
    '--ephemeral',
    '--sandbox',
    'read-only',
    '-m',
    codexModel,
    '--output-last-message',
    outputPath,
    buildPrompt(batch),
  ]

  await new Promise((resolve, reject) => {
    const child = spawn('codex', args, {
      cwd: rootDir,
      stdio: ['ignore', 'pipe', 'pipe'],
    })
    let stdout = ''
    let stderr = ''
    child.stdout.on('data', (chunk) => {
      if (stdout.length < 8000) stdout += String(chunk)
    })
    child.stderr.on('data', (chunk) => {
      if (stderr.length < 8000) stderr += String(chunk)
    })
    const timeout = setTimeout(() => {
      child.kill('SIGTERM')
      reject(new Error(`Codex batch timed out after ${batchTimeoutMs} ms`))
    }, batchTimeoutMs)
    child.on('error', (error) => {
      clearTimeout(timeout)
      reject(error)
    })
    child.on('close', (code, signal) => {
      clearTimeout(timeout)
      if (code === 0) {
        resolve()
        return
      }
      const details = [stderr.trim(), stdout.trim()].filter(Boolean).join('\n')
      reject(new Error(`Codex exited with code ${code ?? 'null'} signal ${signal ?? 'null'}${details ? `\n${details}` : ''}`))
    })
  })

  return parseJsonArrayFromText(await readFile(outputPath, 'utf8'))
}

async function loadVerbChunks() {
  const files = (await readdir(verbsDir))
    .filter((file) => file.endsWith('.json'))
    .sort((left, right) => left.localeCompare(right, undefined, { numeric: true }))
  const chunks = []
  for (const file of files) {
    const filePath = path.join(verbsDir, file)
    chunks.push({ file, filePath, records: JSON.parse(await readFile(filePath, 'utf8')) })
  }
  return chunks
}

async function loadCache() {
  try {
    return JSON.parse(await readFile(cachePath, 'utf8'))
  } catch {
    return {}
  }
}

async function main() {
  await mkdir(cacheDir, { recursive: true })
  const chunks = await loadVerbChunks()
  const cache = await loadCache()

  const allTargets = chunks
    .flatMap((chunk) => chunk.records)
    .filter((verb) => {
      if (rankSet) return rankSet.has(verb.frequencyRank)
      return verb.frequencyRank >= minRank && verb.frequencyRank <= maxRank
    })
  const weakTargets = allTargets.filter(needsRewrite)
  const pending = weakTargets.filter((verb) => !isValidCandidate(cache[verb.frequencyRank]))

  console.log(
    rankSet
      ? `Custom ranks (${rankSet.size} requested). Found: ${allTargets.length}. Need rewrite: ${weakTargets.length}. Pending generation: ${pending.length}.`
      : `Rank range ${minRank}-${maxRank}. In range: ${allTargets.length}. Need rewrite: ${weakTargets.length}. Pending generation: ${pending.length}.`
  )

  for (let i = 0; i < pending.length; i += batchSize) {
    const slice = pending.slice(i, i + batchSize)
    const totalBatches = Math.ceil(pending.length / batchSize)
    const currentBatch = Math.floor(i / batchSize) + 1
    console.log(`Batch ${currentBatch}/${totalBatches} (${slice.length} verbs)...`)
    const missing = new Map(slice.map((verb) => [verb.frequencyRank, verb]))

    for (let attempt = 1; attempt <= maxRetries && missing.size > 0; attempt += 1) {
      try {
        const generated = await runCodexBatch(Array.from(missing.values()))
        for (const item of generated) {
          const rank = Number(item?.frequencyRank)
          if (!missing.has(rank) || !isValidCandidate(item)) continue
          cache[rank] = sanitizeCandidate(item)
          missing.delete(rank)
        }
      } catch (error) {
        console.error(`  attempt ${attempt} failed: ${error.message}`)
        if (isQuotaOrGateError(error)) {
          throw new Error('Stopping metadata rewrite because Codex is quota-limited or blocked by an access challenge.')
        }
      }

      await writeFile(cachePath, `${JSON.stringify(cache, null, 2)}\n`, 'utf8')
      if (missing.size > 0) {
        console.log(`  unresolved after attempt ${attempt}: ${missing.size}`)
      }
    }

    if (missing.size > 0) {
      console.warn(`Batch ${currentBatch}: keeping existing metadata for unresolved ranks ${Array.from(missing.keys()).join(', ')}`)
      if (stopOnBatchFailure) {
        console.warn('Stopping after unresolved batch. Re-run with a narrower RANKS list or STOP_ON_BATCH_FAILURE=0 to continue.')
        break
      }
    }
  }

  let changed = 0
  for (const chunk of chunks) {
    let chunkChanged = false
    for (const verb of chunk.records) {
      const inScope = rankSet
        ? rankSet.has(verb.frequencyRank)
        : verb.frequencyRank >= minRank && verb.frequencyRank <= maxRank
      if (!inScope || !needsRewrite(verb)) continue

      const replacement = cache[verb.frequencyRank]
      if (!isValidCandidate(replacement)) continue
      const sanitized = sanitizeCandidate(replacement)
      verb.aspect = sanitized.aspect
      if (sanitized.aspectPair) {
        verb.aspectPair = sanitized.aspectPair
      } else {
        delete verb.aspectPair
      }
      verb.forms = sanitized.forms
      verb.notes = (verb.notes ?? []).filter((note) => !/generated by fallback|should be checked/i.test(note))
      if (verb.notes.length === 0) {
        verb.reviewStatus = 'wiktionary-enriched'
      }
      changed += 1
      chunkChanged = true
    }
    if (chunkChanged) {
      await writeFile(chunk.filePath, `${JSON.stringify(chunk.records, null, 2)}\n`, 'utf8')
    }
  }

  await writeFile(cachePath, `${JSON.stringify(cache, null, 2)}\n`, 'utf8')
  console.log(`Applied ${changed} metadata/form rewrites using Codex ${codexModel}.`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
